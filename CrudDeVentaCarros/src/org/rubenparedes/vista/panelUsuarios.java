    public void mostrarUsuarioTabla(){
        controlador.enlistarUsuarios();
        
        String[] encabezados = {"Id usuario", "Usuario", "Contraseña", "Id cliente", "Estado activo", "Rol"};
        int cantidadDeRegistro =  controlador.cantidadDeUsuarios();
        DefaultTableModel cabecera = new DefaultTableModel (encabezados,cantidadDeRegistro);
            
        tbl_usuarios.setModel(cabecera);
        TableModel tablaDeUsuario = tbl_usuarios.getModel ();
            for(int i =0; i < cantidadDeRegistro; i++){
                Usuario usuario =  controlador.listaDeUsuarios().get(i);
                tablaDeUsuario.setValueAt(usuario.getId_usuario(),i,0);
                tablaDeUsuario.setValueAt(usuario.getUsuario(), i, 1);
                tablaDeUsuario.setValueAt(usuario.getContraseña(), i, 2);
                tablaDeUsuario.setValueAt(usuario.getId_cliente(), i, 3);  
                tablaDeUsuario.setValueAt(usuario.isEstado_activo(), i, 4);
                tablaDeUsuario.setValueAt(usuario.isRol(), i, 5);
            }
    }

