    public void mostrarCarroTabla(){
        controlador.enlistarCarros();
        
        String[] encabezados = {"Id carro", "Carro", "Modelo", "Precio"};
        int cantidadDeRegistro =  controlador.cantidadDeCarros();
        DefaultTableModel cabecera = new DefaultTableModel (encabezados,cantidadDeRegistro);
            
        tbl_carros.setModel(cabecera);
        TableModel tablaDeCarros = tbl_carros.getModel ();
            for(int i =0; i < cantidadDeRegistro; i++){
                Carro carro =  controlador.listaDeCarros().get(i);
                tablaDeCarros.setValueAt(carro.getId_carro(),i,0);
                tablaDeCarros.setValueAt(carro.getCarro(), i, 1);
                tablaDeCarros.setValueAt(carro.getModelo(), i, 2);
                tablaDeCarros.setValueAt(carro.getPrecio(), i, 3);  
            }
    }

