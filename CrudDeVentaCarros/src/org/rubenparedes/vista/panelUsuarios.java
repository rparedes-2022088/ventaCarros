    private void btnBuscarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarUsuarioActionPerformed
        // TODO add your handling code here:
        Usuario usuario = new Usuario();        
        usuario.setId_usuario(Integer.parseInt(txtIdUsuarioBuscar.getText()));
        usuario = controlador.buscarUsuario(usuario);
        if(usuario == null)
            JOptionPane.showMessageDialog(null, "El dato con el id buscado, no se encuentra en la base de datos.");
        else{
            txtIdUsuarioBuscar.setText(String.valueOf(usuario.getId_usuario()));
            txtUsuario.setText(String.valueOf(usuario.getUsuario()));
            txtContraseña.setText(String.valueOf(usuario.getContraseña()));
            txtIdCliente.setText(String.valueOf(usuario.getId_cliente()));
            txtEstado.setText(String.valueOf(usuario.isEstado_activo()));
            txtRol.setText(String.valueOf(usuario.isRol()));
        }
    }//GEN-LAST:event_btnBuscarUsuarioActionPerformed

    private void btnActualizarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarUsuarioActionPerformed
        // TODO add your handling code here:
        Usuario usuario = new Usuario();
        usuario.setId_usuario(Integer.parseInt(txtIdUsuarioBuscar.getText()));
        usuario.setUsuario(txtUsuario.getText());
        usuario.setContraseña(txtContraseña.getText());
        usuario.setId_cliente(Integer.parseInt(txtIdCliente.getText()));
        usuario.setEstado_activo(Boolean.parseBoolean(txtEstado.getText()));
        usuario.setRol(Boolean.parseBoolean(txtRol.getText()));
        
        controlador.actualizarUsuario(usuario);
        JOptionPane.showMessageDialog(null,"Ha actualizado los datos en la base");
        tbl_usuarios.setModel(new DefaultTableModel());
        mostrarUsuarioTabla();
        
        txtIdUsuarioBuscar.setText(" ");
        txtUsuario.setText(" ");
        txtContraseña.setText(" ");
        txtIdCliente.setText(" ");
        txtEstado.setText(" ");
        txtRol.setText(" ");
    }//GEN-LAST:event_btnActualizarUsuarioActionPerformed


