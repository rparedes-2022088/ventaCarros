    private void btnBuscarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCarroActionPerformed
        // TODO add your handling code here:
        Carro carro = new Carro();        
        carro.setId_carro(Integer.parseInt(txtIdCarroBuscar.getText()));
        carro = controlador.buscarCarro(carro);
        if(carro == null)
            JOptionPane.showMessageDialog(null, "El dato con el id buscado, no se encuentra en la base de datos.");
        else{
            txtIdCarroBuscar.setText(String.valueOf(carro.getId_carro()));
            txtCarro.setText(String.valueOf(carro.getCarro()));
            txtModelo.setText(String.valueOf(carro.getModelo()));
            txtPrecio.setText(String.valueOf(carro.getPrecio()));
        }
    }//GEN-LAST:event_btnBuscarCarroActionPerformed

    private void btnActualizarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarCarroActionPerformed
        // TODO add your handling code here:
        Carro carro = new Carro();
        carro.setId_carro(Integer.parseInt(txtIdCarroBuscar.getText()));
        carro.setCarro(txtCarro.getText());
        carro.setModelo(txtModelo.getText());
        carro.setPrecio(Float.parseFloat(txtPrecio.getText()));
        
        controlador.actualizarCarro(carro);
        JOptionPane.showMessageDialog(null,"Ha actualizado los datos en la base");
        tbl_carros.setModel(new DefaultTableModel());
        mostrarCarroTabla();
        
        txtIdCarroBuscar.setText(" ");
        txtCarro.setText(" ");
        txtModelo.setText(" ");
        txtPrecio.setText(" ");
    }//GEN-LAST:event_btnActualizarCarroActionPerformed


