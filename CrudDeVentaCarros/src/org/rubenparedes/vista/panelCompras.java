    private void btnAgregarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCompraActionPerformed
        // TODO add your handling code here:
        Compra compra = new Compra();
        compra.setFecha_de_compra(txtFechaCompra.getText());
        compra.setId_cliente(Integer.parseInt(txtIdClienteCompra.getText()));
        controlador.agregarCompra(compra);
        
        txtFechaCompra.setText(" ");
        txtIdClienteCompra.setText(" ");
        tbl_compras.setModel(new DefaultTableModel());
        mostrarCompraTabla();
    }//GEN-LAST:event_btnAgregarCompraActionPerformed

    private void btnBuscarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCompraActionPerformed
        // TODO add your handling code here:
        Compra compra = new Compra();        
        compra.setId_compra(Integer.parseInt(txtIdCompraBuscar.getText()));
        compra = controlador.buscarCompra(compra);
        if(compra == null)
            JOptionPane.showMessageDialog(null, "El dato con el id buscado, no se encuentra en la base de datos.");
        else{
            txtIdCompraBuscar.setText(String.valueOf(compra.getId_compra()));
            txtFechaCompra.setText(String.valueOf(compra.getFecha_de_compra()));
            txtIdClienteCompra.setText(String.valueOf(compra.getId_cliente()));
        }
    }//GEN-LAST:event_btnBuscarCompraActionPerformed

    private void btnActualizarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarCompraActionPerformed
        // TODO add your handling code here:
        Compra compra = new Compra();
        compra.setId_compra(Integer.parseInt(txtIdCompraBuscar.getText()));
        compra.setFecha_de_compra(txtFechaCompra.getText());
        compra.setId_cliente(Integer.parseInt(txtIdClienteCompra.getText()));
        
        controlador.actualizarCompra(compra);
        JOptionPane.showMessageDialog(null,"Ha actualizado los datos en la base");
        tbl_compras.setModel(new DefaultTableModel());
        mostrarCompraTabla();
        
        txtIdCompraBuscar.setText(" ");
        txtFechaCompra.setText(" ");
        txtIdClienteCompra.setText(" ");
    }//GEN-LAST:event_btnActualizarCompraActionPerformed


