    public void mostrarCompraTabla(){
        controlador.enlistarCompras();
        
        String[] encabezados = {"Id compra", "Fecha Compra", "Id cliente"};
        int cantidadDeRegistro =  controlador.cantidadDeCompras();
        DefaultTableModel cabecera = new DefaultTableModel (encabezados,cantidadDeRegistro);
            
        tbl_compras.setModel(cabecera);
        TableModel tablaDeCompras = tbl_compras.getModel ();
            for(int i =0; i < cantidadDeRegistro; i++){
                Compra compra =  controlador.listaDeCompras().get(i);
                tablaDeCompras.setValueAt(compra.getId_compra(),i,0);
                tablaDeCompras.setValueAt(compra.getFecha_de_compra(), i, 1);
                tablaDeCompras.setValueAt(compra.getId_cliente(), i, 2);
            }
    }

